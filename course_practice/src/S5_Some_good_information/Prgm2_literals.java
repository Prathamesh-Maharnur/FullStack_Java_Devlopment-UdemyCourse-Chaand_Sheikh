class Prgm2_literals{
    public static void main(String args[]){
        int i = 100;
        int j = 0100;
        System.out.println("i = "+i+", j = "+j);

        i = 0b100011001;
        System.out.println("i = "+i);

        System.out.println("\n \" is double quote symbol!!!");
        System.out.println("hello\bfriends");
        System.out.println("hello friends");

    }
}