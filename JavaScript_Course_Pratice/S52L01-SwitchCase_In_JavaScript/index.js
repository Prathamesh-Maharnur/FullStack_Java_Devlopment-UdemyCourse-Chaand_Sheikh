const grade = 'B';

switch(grade){
    case 'A':
        console.log("Excellent");
        break;
    case 'B':
        console.log("Good");
        break;
    case 'C':
        console.log("Average");
        break;
    case "D":
        console.log("Bad");
        break;
    case 'E':
        console.log("Fail");
        break;
    default:
        console.log("Invalid grade");   
}